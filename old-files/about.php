  				<div id='RobotDescription'>

              <h1>Our Robots!</h1>

              <h2>2014 Robot</h2>

              <p>This robot was designed for the FIRST Aerial Assist® challenge in 2014. That year the team set goals to do three things: drive, throw over the truss, and throw into the goal. Miraculously, we were able to do all three. To drive we used the chassis from the Andy Mark kit of parts. Everything is from the kit other than our new gears so we can drive at a faster gear ratio. To set up for our throws we two cameras running tracking software written in Python, powered by two PCduinos. With our tracking possibilities, we could accurately determine our and other object’s positions, track the goal (and determine if it is hot), the truss, the ball game piece, and other robot’s bumpers.

              To throw, our robot had two of what we call grabbers and an arm. Once aligned to pick up the game piece, the grabbers surrounded the ball and used pneumatics to enclose the ball. Once it has held, two van door motors lifted the ball overhead into the arm and cocked the the self-contained springs. Along with the self-contained springs we used latex bands to easily adjust the throwing force. When fully cocked, a limit switch was activated, stopping the motors from continuing. In the cocked position we could drive and adjust our shot. The grabbers were driven forward and out of the way which then triggered the arm, catapulting it forward.</p>

