              <h1>Our Robots!</h1>

              <h2>2015 Robot</h2>

              <p>This robot was designed for the FIRST Recycle Rush® challenge in 2015. That year, we had a whole new approach to our software, specifically for our driver station. For several years we had the normal driver station off of our laptop, right next to the driver and operators, but we realized something funny: no one ever looks at the screen! Everyone’s eyes are always locked on the robot, so that year we developed a system to convey information in a simplistic, visible, and communicative way through a system of LEDs and micro processors. Our robot was equipped with two sets of two custom PCBs. One set of PCBs is a circular board that brightly displays data, like how much power is being driven through our motors and its own direction, straight from the PWM circuits. The other set of PCBs, is a straight board with 15 LEDs that displays information such as successful alignments being driven through the vision processing software. </p>

