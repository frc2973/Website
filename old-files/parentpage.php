    <div class='questionContainer'>
      <div class='question'><h3>What is the meeting schedule?</h3></div>
      <div class='answer'>
        <p>There are two competitions – one in the spring (FIRST) and one in the fall (BEST). The spring one is more demanding and the main focus of the team. During “build season” for FIRST Robotics (January and March), the team typically meets at “the pit” (300 Dunlop Blvd) at the following times:</p>

          <ul>Monday, 4-6:30 pm</ul>
          <ul>Tuesday, 4-8:30 pm</ul>
          <ul>Thursday, 6-8:30 pm</ul>
          <ul>Saturday, 10 am – 3 or 4 pm</ul>

        <p>During “build season” for BEST Robotics (September and October), the team typically meets a few times a week and occasional Saturdays at times similar to above.</p>
        <p>Outside of build season, the team typically meets about two times a week.</p>
        <p>Exact schedules are communicated weekly via a student “Group Me” and student/parent email. Ask your student if they are signed up for the student’s “Group Me” messages. That is usually the most up-to-date information. You may also want to check the calendar information on this website.</p>
      </div>
    </div>
    <div class='questionContainer'>
      <div class='question'><h3>Does my child have to be at every meeting and/or participate in both competitions?</h3></div>
      <div class='answer'>
        <p>No, students may choose to participate in BEST, FIRST or both. Of course, the more your child is there the more involved they can become, and some leadership or key roles require regular attendance. The teacher sponsor’s philosophy is that it’s ok to only come a couple times a week as this allows students to at least learn and participate a little and to determine if they’d like to be more available in future years.</p>
        <p>Note that the FIRST build season is an intense 6 weeks with meetings at least 5 days a week (including Saturdays) from early January through February with competition(s) in March. If your child has a significant daily conflict during this time it may be better to wait until the next year and/or become involved in the Fall robotics competition (called BEST). The Fall season is also a smaller competition and does not have as many practices.</p>
      </div>
    </div>
    <div class='questionContainer'>
      <div class='question'><h3>Does my child have to know electronics? Programming? Hardware? Marketing?</h3></div>
      <div class='answer'>
        <p>No. In many cases, team members or mentors will teach them and/or students can study on their own. Hard-working, self-motivated students who want to learn and contribute to a robotics team are encouraged to join. There are a variety of roles – mechanical, electrical, software, marketing, communications, planning, etc. One of the best things about this competition is that students have the opportunity to work alongside and learn from “mentors” – adults who voluntarily share their expertise with the students. Mentors include parents, robotics team veterans and local engineers.</p>
      </div>
    </div>
    <div class='questionContainer'>
      <div class='question'><h3>How are the meals and snacks handled?</h3></div>
      <div class='answer'>
        <h3>Tuesday night:</h3>
        <p>Pizza ordered for the team. Students need to bring $1 for each slice of pizza and change for vending machine drinks.<p>
        <h3>Saturday lunch:</h3>
        <p>Fast food restaurants. Bring money. Be sure your student knows if they are allowed to transport or ride with other students. Otherwise there will be at least two adults going to lunch and they can provide rides as well.</p>
        <h3>Snacks:</h3>
        <p>In addition to vending machines, there will be typically be water bottles, chips and similar snacks donated by parents. You are welcome to contribute or watch for opportunities to sign up to bring special snacks or meals.</p>
      </div>
    </div>
    <div class='questionContainer'>
      <div class='question'><h3>What about safety?</h3></div>
      <div class='answer'>
        <p>The pit is an operating workshop with equipment that can be dangerous if used inappropriately. There are also sharp objects everywhere including metal fragments on the floor. Everyone in the pit must wear close-toed shoes and must wear safety glasses (available in the pit) when working on equipment. Students will be working with power tools and “real” robots so need to pay attention to the safety rules and be sure they have been trained before operating any equipment. Parents should emphasize to students the responsibility of working safely for their own and the team’s protection. In addition, the team strives to have at least two adults at every meeting.</p>
      </div>
    </div>
    <div class='questionContainer'>
      <div class='question'><h3>What if guests or younger siblings want to observe?</h3></div>
      <div class='answer'>
        <p>See the section on safety. All adult guests must have closed-toe shoes in the pit area. Due to safety issues, younger siblings and anyone without closed-toe shoes can only observe from outside the pit occasionally at drop off/pick up. In addition, there is usually an open house to see the robot in action. The best way for younger siblings to be involved is to attend the competition as only team members and adults should be in the pit area.</p>
      </div>
    </div>
    <div class='questionContainer'>
      <div class='question'><h3>What are the fees to participate?</h3></div>
      <div class='answer'>
        <p>At this time, there is a not a fee to join. Fortunately most of the competition costs are covered by grants and sponsors. (Additional sponsors are always welcome, so please share if you have any leads!). Students are responsible for their meals, team shirts and the cost for transportation and lodging for out-of-town events. One of the competitions is in Huntsville, so fortunately it does not require travel or any significant cost to students.</p>
      </div>
    </div>
    <div class='questionContainer'>
      <div class='question'><h3>What type of parental support is needed?</h3></div>
      <div class='answer'>
        <p>One of the ways most parents need to be involved is providing transportation to and from practices since the team typically meets at “the pit” (300 Dunlop Blvd) and not at either school. Please be courteous of the other adult volunteers and pick up your child on time. Due to the remoteness of the location, we will not leave a student to wait on their own. There are many ways for parents to participate and to support the team. Possible volunteer opportunities include:</p>
        <h3>Technical/Engineering mentor</h3>
        <p>Helping the students design and build the robot including electrical and mechanical engineering and software engineering/programming</p>
        <h3>Non-Technical mentor</h3>
        <p>Helping with marketing, video production, project management, finances and planning</p>
        <h3>General support</h3>
        <p>helping with meal and event coordination, team communications, paperwork and serving as an adult chaperone/supervisor when mentors are unavailable</p>
        <h3>Finding mentors</h3>
        <p>The team is always looking for mentors who are interested in working with the students. Mentors who are regularly available once or more per week during build season can be of great value to the team.</p>
        <h3>Fundraising</h3>
        <p>If you have any suggestions of sponsors or grants, please let us know.</p>
