        <h3>About FIRST</h3>
        <p>
            The FIRST Robotics program has been around since 1989, providing students with a means of using their knowledge and strategic skills.
            It is a competition to build each student in the fields of science, engineering, and technology.
            It is a group of kids working towards one goal, but that goal obtains a myriad of results.
            The mission of FIRST stretches past just building a robot for matches.
            It is about a culture that science and technology can inspire.
            Engineering, mathematics, the academics behind the game: FIRST is creating a culture of young individuals who recognize the joy of innovation and creation.
            As a team of the FRC, the Mad Rockers strive to spread this culture and enthusiasm to their community and, more importantly, the future leaders.
        </p>
        <h4>Find out more below:</h4>
        <a target='_blank' href='https://www.firstinspires.org/'><button class='aboutButton'>FIRST <i class='fa fa-arrow-circle-right'></i></button></a>
        <h3>About BEST</h3>
        <p>
            BEST is a middle and high school robotics competition whose mission is to engage and excite students about engineering, science, and technology as well as inspire them to pursue careers in these fields. Students learn, through participation in BEST's prohect-based STEM program, to analyze and solve problems utilizing the Engineering Design Process.
        </p>
        <h4>Find out more below:</h4>
        <a target='_blank' href='http://best.eng.auburn.edu/'><button class='aboutButton'>BEST <i class='fa fa-arrow-circle-right'></i></button></a>
      </div>
